/* Dalam Operator Aritmatika ada: [ + - * / ]

Keterangan:
    +   <-Tambah
    -   <-Kurang
    *   <-Kali
    /   <-Bagi


*/

// Contoh

var Lima = 5;
var Tiga = 3;
var HasilTambah = Lima + Tiga
var HasilKurang = Lima - Tiga
var HasilKali = Lima * Tiga
var HasilBagi = Lima / Tiga

// **TAMBAH**
console.log("[TAMBAH]   "+Lima+" + "+Tiga+"   Hasilnya adalah= "+HasilTambah)
console.log("[KURANG]   "+Lima+" - "+Tiga+"   Hasilnya adalah= "+HasilKurang)
console.log("[KALI]   "+Lima+" * "+Tiga+"   Hasilnya adalah= "+HasilKali)
console.log("[BAGI]   "+Lima+" / "+Tiga+"   Hasilnya adalah= "+HasilBagi)